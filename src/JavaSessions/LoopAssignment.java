package JavaSessions;

public class LoopAssignment {

	public static void main(String[] args) {
		
		for(int i=1; i<=5; i++){
		System.out.println("I am Batman");
		}
	System.out.println("******************************");	
	
	for(int j=1; j<=9; j++){
		System.out.println("I am Batman" + j);
		}
	System.out.println("******************************");
	
	int k=10;
	do {
		System.out.println(k);
		k--;
		
	} while (k>=1);
	
	System.out.println("******************************");
	
	int l=1;
	while(l<=10) {
		System.out.println("Hello World");
	l++;
	}
	System.out.println("******************************");
	}     
    }

	






