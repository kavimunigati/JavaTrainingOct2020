package JavaSessions;

public class ModOperator {

	public static void main(String[] args) {
		// Mod or Modular is an operator which leaves the reminder
		//and is denoted by "%" symbol
		int a= 9;
	    int b =3;
	    System.out.println(a%b);//0
	    
	    System.out.println(5%2);//1 modular operator shows the reminder left after the division
	    //System.out.println(9%0);// java.lang.ArithmeticException: / by zero
	    //any number divisible by zero is infinity
	    //but java does not recognize infinity it throwsa exception error
	    byte b1=053;
	    System.out.println(b1);//43
				

	}

}
